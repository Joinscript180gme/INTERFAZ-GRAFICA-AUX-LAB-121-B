    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String nombreArchivo="CristianM_futbolistas.dat";
        boolean flag =false;
        ObjectInputStream archivo=null; // de lectura
        ObjectOutputStream archivoAuxiliar=null; // de escritura
        String posicion=PosicionElminarTxt.getText();
        
        try {
            archivo=new ObjectInputStream(new FileInputStream(nombreArchivo));
            archivoAuxiliar=new ObjectOutputStream(new FileOutputStream("copia.dat", true));
            while(true){
                Futbolista fut=(Futbolista) archivo.readObject();
                if(fut.getPosicion().equalsIgnoreCase(posicion)){
                    //eliminar ese futbolista
                }else{
                    archivoAuxiliar.writeObject(fut);
                    flag=true;
                }
            }
        } catch (Exception e) {
        }finally{
            try {
                archivo.close();
            } catch (Exception ex) {
                Logger.getLogger(listaYacciones.class.getName()).log(Level.SEVERE,null,ex);
            }
            try {
                archivoAuxiliar.close();
            } catch (Exception ex) {
                Logger.getLogger(listaYacciones.class.getName()).log(Level.SEVERE,null,ex);
            }
            if(flag!=true){
                JOptionPane.showMessageDialog(null, "NO ENCONTRADO");
            }else{
                JOptionPane.showMessageDialog(null, "ELIMINADO/S");
            }
            File f1=new File(nombreArchivo);
            File f2=new File("copia.dat");
            f1.delete();
            f2.renameTo(f1);
            
            mostrarTabla();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

        public int menorEdad(){
        int menor=9999;
        ObjectInputStream archivo=null;
        String nom="CristianM_futbolistas.dat";
        try {
            archivo=new ObjectInputStream(new FileInputStream(nom));
            DateTimeFormatter fmt = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            
            while(true){
                Futbolista fut=(Futbolista) archivo.readObject();
                LocalDate fechaNac = LocalDate.parse(fut.getFecha_nac(), fmt);
                LocalDate ahora = LocalDate.now();
                Period periodo = Period.between(fechaNac, ahora);
                if(periodo.getYears()<menor){
                    menor=periodo.getYears();
                }
            }
        } catch (Exception e) {
        }finally{
            try {
                archivo.close();
            } catch (Exception ex) {
                Logger.getLogger(listaYacciones.class.getName()).log(Level.SEVERE,null,ex);
            }
            return menor;
        }
    }

