    public void mostrarTabla(){
        tabla.setColumnCount(0);
        tabla.setRowCount(0);
        tabla.addColumn("CI");
        tabla.addColumn("NOMBRE");
        tabla.addColumn("APELLIDO");
        tabla.addColumn("FECHA NACIMIENTO");
        tabla.addColumn("POSICION");
        tabla.addColumn("TITULOS");
        tabla.addColumn("NRO CASACA");
        this.futbolistas.setModel(tabla);
        ObjectInputStream archivo=null;
        String nom="CristianM_futbolistas.dat";
        try {
            archivo=new ObjectInputStream(new FileInputStream(nom));
            while(true){
                Futbolista fut=new Futbolista();
                fut=(Futbolista)archivo.readObject();
                String v[]=new String[7];
                v[0]=fut.getCi()+"";
                v[1]=fut.getNombre();
                v[2]=fut.getApellido()+"";
                v[3]=fut.getFecha_nac()+"";
                v[4]=fut.getPosicion()+"";
                v[5]=fut.getTitulos_ganados()+"";
                v[6]=fut.getNro_casaca()+"";
                tabla.addRow(v);
                
            }
        } catch (Exception e) {
        }finally{
            try {
                archivo.close();
            } catch (Exception ex) {
                Logger.getLogger(listaYacciones.class.getName()).log(Level.SEVERE,null,ex);
            }
        }
    }
